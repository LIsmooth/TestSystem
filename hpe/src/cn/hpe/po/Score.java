package cn.hpe.po;

public class Score {

	int examno;//考试编号
	int ptscore;//平时成绩
	int etscore; //卷面成绩
	int score;//总成绩
	String examname;//考试名称
	String subjectname;//科目名称
	String examdate;//考试时间
	
	public Score() {
		
	}

	public Score(int examno, int ptscore, int etscore, int score,
			String examname, String subjectname, String examdate) {
		this.examno = examno;
		this.ptscore = ptscore;
		this.etscore = etscore;
		this.score = score;
		this.examname = examname;
		this.subjectname = subjectname;
		this.examdate = examdate;
	}

	public int getExamno() {
		return examno;
	}

	public void setExamno(int examno) {
		this.examno = examno;
	}

	public int getPtscore() {
		return ptscore;
	}

	public void setPtscore(int ptscore) {
		this.ptscore = ptscore;
	}

	public int getEtscore() {
		return etscore;
	}

	public void setEtscore(int etscore) {
		this.etscore = etscore;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

	public String getExamname() {
		return examname;
	}

	public void setExamname(String examname) {
		this.examname = examname;
	}

	public String getSubjectname() {
		return subjectname;
	}

	public void setSubjectname(String subjectname) {
		this.subjectname = subjectname;
	}

	public String getExamdate() {
		return examdate;
	}

	public void setExamdate(String examdate) {
		this.examdate = examdate;
	}
	
	
	
}
